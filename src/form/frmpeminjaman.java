     private void tampilId(){
        int row = tbl_pnjm.getRowCount();
        int col = tbl_pnjm.getColumnCount();
        if(row==0){
            txt_id_pnjm.setText("PN-1");
        } else {
        Object[] val =new Object[col];
        for (int k = 0; k < val.length - 1; k++) {
            for (int i = 0; i < row; i++) {
                for (int j = 0; j <col; j++) {
                    val[k] = tbl_pnjm.getValueAt(i, 0);
                    String id = val[k].toString();
                    int ambil = Integer.parseInt(id.substring(3, 4))+1;
                    txt_id_pnjm.setText("PN-"+String.valueOf(ambil));
                    }
            }
            }
        }
    }


    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        try {
            // TODO add your handling code here:
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            String tpinjam = String.valueOf(sdf.format(jPinjam.getDate()));
            Date pinjam = sdf.parse(tpinjam);
            Calendar cal = Calendar.getInstance();
            cal.setTime(pinjam);
            cal.add(Calendar.DAY_OF_MONTH, 7);
            String tHrus = String.valueOf(sdf.format(cal.getTime()));

            pnjm.insert(txt_id_pnjm.getText(), txt_id_an.getText(), txt_id_buku.getText(), tpinjam, tHrus);
            pnjm.tampilData();
            New();
        } catch (ParseException ex) {
            Logger.getLogger(frmpeminjaman.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Ada data yg belum dinput!!");
        }
}//GEN-LAST:event_btn_simpanActionPerformed

